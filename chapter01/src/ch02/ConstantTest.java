package ch02;

public class ConstantTest {
// 코드의 시작점(main)
	public static void main(String[] args) {
		//변수 != 상수 (상수는 변하지 않는수)
		//원주율 3.14. 1년 12개월
		//예약어 final 사용
		//권장 사항 - 상수는 대문자사용하고 연결에 단어는 _ 구분

		final int MAX_NUM = 12; // 상수에 선언과 초기화
		//MAX_NUM = 200; // 한번 초기화된(값을 넣다) 값은 변경할수 없다
		
		final int MIM_NUM;//상수에 선언
		MIM_NUM = 1;// 상수는 선언후에 단한번만 초기화 할수있다
		
		
		 
	}//end of main

}//end of class
